	public void show(TableRow tableRow, boolean canAmend) {
		this.tableRow = tableRow;
		// periodTypeText.setEditable(canAmend);
		periodNumberText.setEditable(canAmend);
		fromDateText.setEditable(canAmend);
		toDateText.setEditable(canAmend);
		period = null;
		if (tableRow != null) {

			PeriodPK pk = (PeriodPK) tableRow.getPrimaryKey();

			try {
				InitialContext jndi = fina2.Main.getJndiContext();

				Object ref = jndi.lookup("fina2/period/Period");
				PeriodHome home = (PeriodHome) PortableRemoteObject.narrow(ref, PeriodHome.class);

				period = home.findByPrimaryKey(pk);

				ref = jndi.lookup("fina2/period/PeriodType");
				PeriodTypeHome typeHome = (PeriodTypeHome) PortableRemoteObject.narrow(ref, PeriodTypeHome.class);

				typePK = period.getType();
				PeriodType periodType = typeHome.findByPrimaryKey(typePK);

				periodNumberText.setText(period.getPeriodNumber());
				fromDateText.setText(period.getFromDate(main.getLanguageHandle()));
				toDateText.setText(period.getToDate(main.getLanguageHandle()));

			} catch (Exception e) {
				Main.generalErrorHandler(e);
				return;
			}
		}
		setLocationRelativeTo(getParent());
		show();
	}

