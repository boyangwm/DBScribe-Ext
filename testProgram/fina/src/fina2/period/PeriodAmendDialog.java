	private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-
		// FIRST
		// :
		// event_okButtonActionPerformed

		if (typePK == null)
			return;
		try {
			InitialContext jndi = fina2.Main.getJndiContext();
			Object ref = jndi.lookup("fina2/period/Period");
			PeriodHome home = (PeriodHome) PortableRemoteObject.narrow(ref, PeriodHome.class);

			try {
				if (period == null) {
					period = home.create();
				}
			} catch (Exception e) {
				if (tableRow == null)
					period = null;
				Main.generalErrorHandler(e);
				return;
			}
			UserTransaction trans = main.getUserTransaction(jndi);
			trans.begin();

			try {

				if (typePK != null)
					period.setType(typePK);
				period.setPeriodNumber(periodNumberText.getText());
				period.setFromDate(main.getLanguageHandle(), fromDateText.getText());
				period.setToDate(main.getLanguageHandle(), toDateText.getText());

				trans.commit();
			} catch (java.text.ParseException e) {
				Main.errorHandler(null, Main.getString("fina2.title"), Main.getString("fina2.invalidDateFormat"));
				trans.rollback();
				if (tableRow == null) {
					period.remove();
					period = null;
				}
				return;
			} catch (Exception e) {
				Main.generalErrorHandler(e);
				trans.rollback();
				if (tableRow == null) {
					period.remove();
					period = null;
				}
				return;
			}
			if (tableRow == null) {
				tableRow = new TableRowImpl(period.getPrimaryKey(), 4);
				if (typePK != null)
					tableRow.setValue(0, typeList.getSelectedItem().toString());
				else
					tableRow.setValue(0, "");
				tableRow.setValue(1, period.getPeriodNumber());
				tableRow.setValue(2, period.getFromDate(main.getLanguageHandle()));
				tableRow.setValue(3, period.getToDate(main.getLanguageHandle()));
			} else {
				if (typePK != null)
					tableRow.setValue(0, typeList.getSelectedItem().toString());
				else
					tableRow.setValue(0, "");
				tableRow.setValue(1, period.getPeriodNumber());
				tableRow.setValue(2, period.getFromDate(main.getLanguageHandle()));
				tableRow.setValue(3, period.getToDate(main.getLanguageHandle()));
			}
		} catch (Exception e) {
			Main.generalErrorHandler(e);
		}
		dispose();
	}// GEN-LAST:event_okButtonActionPerformed

