	public void show(TableRow tableRow, boolean canAmend) {
		this.tableRow = tableRow;
		codeText.setEditable(canAmend);
		descriptionText.setEditable(canAmend);
		periodType = null;
		if (tableRow != null) {

			PeriodTypePK pk = (PeriodTypePK) tableRow.getPrimaryKey();

			try {
				InitialContext jndi = fina2.Main.getJndiContext();

				Object ref = jndi.lookup("fina2/period/PeriodType");
				PeriodTypeHome home = (PeriodTypeHome) PortableRemoteObject.narrow(ref, PeriodTypeHome.class);

				periodType = home.findByPrimaryKey(pk);

				codeText.setText(periodType.getCode());
				descriptionText.setText(periodType.getDescription(main.getLanguageHandle()));

			} catch (Exception e) {
				Main.generalErrorHandler(e);
				return;
			}
		}
		setLocationRelativeTo(getParent());
		show();
	}

