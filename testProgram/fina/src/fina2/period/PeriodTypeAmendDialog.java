	private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {
		// Code and Description validation
		if (!ui.isValidCode(codeText.getText())) {
			if (!ui.isValidLength(codeText.getText(), false)) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.web.banktype.code.length"));
				return;
			} else if (codeText.getText().length() == 0) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.definiton.code"));
				return;
			} else {

				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.invalidCode"));
				return;
			}

		}
		if (!ui.isValidLength(descriptionText.getText(), true)) {
			ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.web.banktype.description.length255"));
			return;
		}
		if (descriptionText.getText().trim().equals("")) {
			ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.pleaseEnterDescription"));
			return;
		}
		try {
			InitialContext jndi = fina2.Main.getJndiContext();
			Object ref = jndi.lookup("fina2/period/PeriodType");
			PeriodTypeHome home = (PeriodTypeHome) PortableRemoteObject.narrow(ref, PeriodTypeHome.class);

			try {
				if (periodType == null) {
					periodType = home.create();
				}
			} catch (Exception e) {
				if (tableRow == null)
					periodType = null;
				Main.generalErrorHandler(e);
				return;
			}
			UserTransaction trans = main.getUserTransaction(jndi);
			trans.begin();

			try {
				// bankType.setCode(codeText.getText());
				periodType.setCode(codeText.getText());
				System.out.println("s" + codeText.getText());
				periodType.setDescription(main.getLanguageHandle(), descriptionText.getText());
				try {
					trans.commit();
				} catch (Exception tex) {
				}
			} catch (Exception e) {
				Main.generalErrorHandler(e);
				try {
					trans.rollback();
				} catch (Exception tex) {
				}
				if (tableRow == null) {
					periodType.remove();
					periodType = null;
				}
				return;
			}
			if (tableRow == null) {
				tableRow = new TableRowImpl(periodType.getPrimaryKey(), 2);
				tableRow.setValue(0, periodType.getCode());
				tableRow.setValue(1, periodType.getDescription(main.getLanguageHandle()));
			} else {
				tableRow.setValue(0, periodType.getCode());
				tableRow.setValue(1, periodType.getDescription(main.getLanguageHandle()));
			}
		} catch (Exception e) {
			Main.generalErrorHandler(e);
		}
		dispose();
	}// GEN-LAST:event_okButtonActionPerformed

