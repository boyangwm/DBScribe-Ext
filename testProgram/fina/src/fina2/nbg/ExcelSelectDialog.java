    public void show() {
        try {
            InitialContext jndi = fina2.Main.main.getJndiContext();
            Object ref = jndi.lookup("fina2/bank/BankSession");
            BankSessionHome home = (BankSessionHome) PortableRemoteObject
                    .narrow(ref, BankSessionHome.class);

            BankSession session = home.create();
            Collection rows = session.getBanksRows(main.getUserHandle(), main
                    .getLanguageHandle());

            for (Iterator iter = rows.iterator(); iter.hasNext();) {
                TableRowImpl row = (TableRowImpl) iter.next();
                row.setDefaultCol(1);
            }

            bankList.setModel(new javax.swing.DefaultComboBoxModel(new Vector(
                    rows)));
            //bankList.setSelectedIndex(0);

            Vector types = new Vector();
            types.add("Financial");
            types.add("Supervision");
            typeList.setModel(new javax.swing.DefaultComboBoxModel(types));
            //typeList.setSelectedIndex(0);

            pack();
            setLocationRelativeTo(getParent());
            super.show();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "error\n" + e.getMessage());
            System.exit(1);
        }
    }

