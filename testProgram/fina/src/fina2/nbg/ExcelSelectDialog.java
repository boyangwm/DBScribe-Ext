    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TableRowImpl row = (TableRowImpl) bankList.getSelectedItem();
        String name = null;
        try {
            int y = Integer.valueOf(yearText.getText()).intValue();
            if (typeList.getSelectedIndex() == 0) {
                if (y >= 2002)
                    name = "F&S Files " + monthText.getText()
                            + yearText.getText() + "\\F";
                else
                    name = "R&S Files " + monthText.getText()
                            + yearText.getText() + "\\R";
            } else {
                if (y >= 2002)
                    name = "F&S Files " + monthText.getText()
                            + yearText.getText() + "\\III Dekada\\S3";
                else
                    name = "R&S Files " + monthText.getText()
                            + yearText.getText() + "\\S";
            }
        } catch (Exception e) {
        }
        name += row.getValue(0);
        name += monthText.getText();
        name += yearText.getText() + ".xls";
        System.out.println("\"" + main.getProperties().getProperty("excel.exe")
                + "\" \"" + main.getProperties().getProperty("xls.path") + "\\"
                + name + "\"");
        try {
            Runtime.getRuntime().exec(
                    "\"" + main.getProperties().getProperty("excel.exe")
                            + "\" \""
                            + main.getProperties().getProperty("xls.path")
                            + "\\" + name + "\"");
        } catch (Exception e) {
            System.out.println("Wrong Path !");
            //            ui.showMessageBox(this, ui.getString("fina2.nbg.fileNotFound"));
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

