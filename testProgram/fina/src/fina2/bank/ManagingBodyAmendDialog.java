	public void show(TableRow tableRow, boolean canAmend) {
		this.tableRow = tableRow;
		this.canAmend = canAmend;
		managingBodyText.setEditable(canAmend);
		managingBody = null;
		if (tableRow != null) {

			ManagingBodyPK pk = (ManagingBodyPK) tableRow.getPrimaryKey();

			try {
				InitialContext jndi = fina2.Main.getJndiContext();
				Object ref = jndi.lookup("fina2/bank/ManagingBody");
				ManagingBodyHome home = (ManagingBodyHome) PortableRemoteObject.narrow(ref, ManagingBodyHome.class);

				managingBody = home.findByPrimaryKey(pk);

				managingBodyText.setText(managingBody.getManagingBody(main.getLanguageHandle()));

			} catch (Exception e) {
				Main.generalErrorHandler(e);
				return;
			}
		} else {
			managingBodyText.setText("");
		}
		setLocationRelativeTo(getParent());
		show();
	}

