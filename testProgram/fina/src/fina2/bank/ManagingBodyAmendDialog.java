	private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-
		// FIRST
		// :
		// event_okButtonActionPerformed
		if (canAmend) {
			if (managingBodyText.getText().trim().equals("")) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.bank.pleaseEnterCity"));
				return;
			}
			try {
				InitialContext jndi = fina2.Main.getJndiContext();
				Object ref = jndi.lookup("fina2/bank/ManagingBody");
				ManagingBodyHome home = (ManagingBodyHome) PortableRemoteObject.narrow(ref, ManagingBodyHome.class);
				try {
					if (managingBody == null) {
						managingBody = home.create();
					}
				} catch (Exception e) {
					if (tableRow == null)
						managingBody = null;
					Main.generalErrorHandler(e);
					return;
				}

				UserTransaction trans = main.getUserTransaction(jndi);
				trans.begin();

				try {
					if (managingBody == null) {
						managingBody = home.create();
					}
					managingBody.setManagingBody(main.getLanguageHandle(), managingBodyText.getText());
					trans.commit();
				} catch (Exception e) {
					trans.rollback();
					if (managingBody == null) {
						managingBody.remove();
						managingBody = null;
					}
					Main.generalErrorHandler(e);
					return;
				}
				if (tableRow == null) {
					tableRow = new TableRowImpl(managingBody.getPrimaryKey(), 1);
					tableRow.setValue(0, managingBody.getManagingBody(main.getLanguageHandle()));
				} else {
					tableRow.setValue(0, managingBody.getManagingBody(main.getLanguageHandle()));
				}
			} catch (Exception e) {
				Main.generalErrorHandler(e);
			}
		}
		dispose();
	}// GEN-LAST:event_okButtonActionPerformed

