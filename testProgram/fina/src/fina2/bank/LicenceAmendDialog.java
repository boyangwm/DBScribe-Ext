	private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-
		// FIRST
		// :
		// event_okButtonActionPerformed

		if (canAmend) {
			// ===========licence type validation=================
			if (licenceTypeText.getText().length() == 0) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.bank.LicenceType"));
				return;
			}
			// ===========code validation=================
			if (!ui.isValidCode(licenceCodeText.getText())) {
				if (!ui.isValidLength(licenceCodeText.getText(), false)) {
					ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.invalidLengthCode"));
					return;
				} else if (licenceCodeText.getText().length() == 0) {
					ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.definiton.code"));
					return;
				} else {
					ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.invalidCode"));
					return;
				}
			}
			// ===========date validation=================
			if (licenceDateText.getText().trim().length() == 0) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.bank.LicenceType.date"));
				return;
			}

			if (!ui.isCorrectDate(licenceDateText.getText().trim())) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.invalidDateFormat"));
				return;
			}
			// ===========change of date validation=================
			if (!ui.isCorrectDate(licenceDateOfChangeText.getText().trim()) && licenceDateOfChangeText.getText().trim().length() > 0) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.invalidDateFormat"));
				return;
			}
			// ===========reason validation=================
			if (!ui.isValidLength(licenceReasonText.getText(), true)) {
				ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.bank.LicenceType.reason"));
				return;
			}

			try {
				InitialContext jndi = fina2.Main.getJndiContext();
				Object ref = jndi.lookup("fina2/bank/Licence");
				LicenceHome home = (LicenceHome) PortableRemoteObject.narrow(ref, LicenceHome.class);

				if (node == null) {
					licence = home.create();
				}
				UserTransaction trans = main.getUserTransaction(jndi);
				trans.begin();

				try {
					if (licenceTypePK != null)
						licence.setTypePK(licenceTypePK);
					licence.setCode(licenceCodeText.getText().trim());
					licence.setDate(main.getLanguageHandle(), ui.stringFormatedToServerDataFormat(licenceDateText.getText().trim()));
					licence.setDateOfChange(main.getLanguageHandle(), ui.stringFormatedToServerDataFormat(licenceDateOfChangeText.getText().trim()));
					licence.setReason(main.getLanguageHandle(), licenceReasonText.getText().trim());
					if (operationalCheckBox.isSelected())
						licence.setOperational(1);
					else
						licence.setOperational(0);
					licence.setBankPK(pk);

					if (licenceTypePK == null) {
						trans.rollback();
						if (node == null) {
							licence.remove();
							licence = null;
						}

					}

					trans.commit();
				} catch (Exception e) {
					trans.rollback();
					if (node == null) {
						licence.remove();
						licence = null;
					}
					Main.generalErrorHandler(e);
					return;
				}
				if (node == null) {
					node = new Node(licence.getPrimaryKey(), licenceTypeText.getText(), new Integer(2));
					licenceTypePK = licence.getTypePK();
				} else {
					node.setLabel(licenceTypeText.getText());
					licenceTypePK = licence.getTypePK();
				}
			} catch (Exception e) {
				Main.generalErrorHandler(e);
			}
		}

		dispose();

	}// GEN-LAST:event_okButtonActionPerformed

