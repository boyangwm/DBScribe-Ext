	public void show(Node node, BankPK pk, boolean canAmend) {
		this.node = node;
		this.pk = pk;
		this.canAmend = canAmend;

		selLicenceTypeButton.setEnabled(canAmend);
		licenceCodeText.setEditable(canAmend);
		licenceDateText.setEditable(canAmend);
		licenceDateOfChangeText.setEditable(canAmend);
		licenceReasonText.setEditable(canAmend);
		operationalCheckBox.setEnabled(canAmend);

		if (node != null) {

			licencePK = (LicencePK) node.getPrimaryKey();

			try {
				InitialContext jndi = fina2.Main.getJndiContext();

				Object ref = jndi.lookup("fina2/bank/Licence");
				LicenceHome home = (LicenceHome) PortableRemoteObject.narrow(ref, LicenceHome.class);

				licence = home.findByPrimaryKey(licencePK);

				ref = jndi.lookup("fina2/bank/LicenceType");

				LicenceTypeHome licenceTypeHome = (LicenceTypeHome) PortableRemoteObject.narrow(ref, LicenceTypeHome.class);
				licenceTypePK = licence.getTypePK();
				LicenceType licenceType = licenceTypeHome.findByPrimaryKey(licenceTypePK);

				licenceTypeText.setText(licenceType.getDescription(main.getLanguageHandle()));
				licenceCodeText.setText(licence.getCode());
				licenceDateText.setText(ui.stringToCurrentDateFormat(licence.getDate(main.getLanguageHandle())));
				licenceDateOfChangeText.setText(ui.stringToCurrentDateFormat(licence.getDateOfChange(main.getLanguageHandle())));
				licenceReasonText.setText(licence.getReason(main.getLanguageHandle()));
				if (licence.getOperational() == 1) {
					operationalCheckBox.setSelected(true);
				} else {
					operationalCheckBox.setSelected(false);
				}

			} catch (Exception e) {
				Main.generalErrorHandler(e);
				return;
			}
		} else {
			licenceTypeText.setText("");
			licenceCodeText.setText("");
			licenceDateText.setText("");
			licenceDateOfChangeText.setText("");
			licenceReasonText.setText("");
			operationalCheckBox.setSelected(false);
		}
		setLocationRelativeTo(getParent());
		show();
	}

