	private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {

		if (!ui.isValidLength(descriptionText.getText(), true)) {

			ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.web.banktype.description.length"));
			return;
		} else if (descriptionText.getText().trim().length() == 0) {
			ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.pleaseEnterDescription"));
			return;
		}
		if (typeList.getSelectedIndex() == 0 && actionText.getText().length() == 0) {
			ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.menu.action"));
			return;
		} else if (typeList.getSelectedIndex() == 1 && applicationText.getText().trim().length() == 0) {
			ui.showMessageBox(null, ui.getString("fina2.title"), ui.getString("fina2.menu.application"));
			return;
		}

		if ((typeList.getSelectedIndex() == 0 && actionKey.trim() != "") || (typeList.getSelectedIndex() == 1)) {

			try {
				InitialContext jndi = fina2.Main.getJndiContext();

				Object ref = jndi.lookup("fina2/ui/menu/Menu");
				MenuHome home = (MenuHome) PortableRemoteObject.narrow(ref, MenuHome.class);

				try {
					if (node == null) {
						MenuPK parentPK = (MenuPK) parent.getPrimaryKey();
						menu = home.create(parentPK);
						// menu.setType(MenuConstants.MENU_ACTION_TYPE);
					}
				} catch (Exception e) {
					if (node == null)
						menu = null;
					Main.generalErrorHandler(e);
					return;
				}
				UserTransaction trans = main.getUserTransaction(jndi);
				trans.begin();
				try {
					menu.setDescription(main.getLanguageHandle(), descriptionText.getText());
					switch (typeList.getSelectedIndex()) {
					case 0:
						menu.setType(MenuConstants.MENU_ACTION_TYPE);
						menu.setActionKey(actionKey); // actionText.getText());
						break;
					case 1:
						menu.setType(MenuConstants.MENU_APPLICATION_TYPE);
						menu.setApplication(applicationText.getText());
						break;
					}
					trans.commit();
				} catch (Exception e) {
					Main.generalErrorHandler(e);
					trans.rollback();
					if (node == null) {
						menu.remove();
						menu = null;
					}
					return;
				}
				if (node == null) {
					node = new Node(menu.getPrimaryKey(), menu.getDescription(main.getLanguageHandle()), new Integer(menu.getType()));
				} else {
					node.setLabel(menu.getDescription(main.getLanguageHandle()));
				}
			} catch (Exception e) {
				Main.generalErrorHandler(e);
			}
			dispose();
			// }
		}

	}// GEN-LAST:event_okButtonActionPerformed

