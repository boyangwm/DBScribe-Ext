	public void show(Node parent, Node node) {
		this.node = node;
		this.parent = parent;

		if (node != null) {

			try {
				InitialContext jndi = fina2.Main.getJndiContext();

				Object ref = jndi.lookup("fina2/ui/menu/Menu");
				MenuHome home = (MenuHome) PortableRemoteObject.narrow(ref, MenuHome.class);

				MenuPK pk = (MenuPK) node.getPrimaryKey();
				menu = home.findByPrimaryKey(pk);

				descriptionText.setText(menu.getDescription(main.getLanguageHandle()));
				switch (menu.getType()) {
				case MenuConstants.MENU_ACTION_TYPE:

					typeList.setSelectedIndex(0);
					applicationText.setText("");
					applicationText.setEditable(false);
					actionKey = menu.getActionKey();

					AbstractAction action = ui.getAllAction(actionKey);
					if (action != null) {
						actionText.setText((String) action.getValue(javax.swing.AbstractAction.NAME));
					} else {
						actionText.setText("#Unknown");
					}

					break;
				case MenuConstants.MENU_APPLICATION_TYPE:

					typeList.setSelectedIndex(1);
					actionText.setText("");
					applicationText.setEditable(true);
					applicationText.setText(menu.getApplication());
					lookupActionButton.setEnabled(false);

					break;
				default:

					typeList.setSelectedIndex(0);
					applicationText.setEditable(false);
				}

			} catch (Exception e) {
				Main.generalErrorHandler(e);
				return;
			}
		}
		setLocationRelativeTo(getParent());
		show();
	}

